import numpy as np

# 兩個圖像的像素值列表
image1_values = [[121, 124, 122], [122, 125, 123], [122, 125, 123], [122, 125, 123], [118, 121, 119], [118, 121, 119], [116, 119, 117], [120, 123, 121], [116, 119, 117], [120, 123, 121], [118, 121, 119], [128, 131, 129], [113, 116, 114], [154, 157, 155], [165, 168, 166], [168, 171, 169], [171, 174, 172], [128, 131, 129], [127, 130, 128]]
image2_values = [[222, 225, 223], [222, 225, 223], [221, 224, 222], [219, 222, 220], [222, 225, 223], [222, 225, 223], [224, 227, 225], [221, 224, 222], [225, 228, 226], [227, 230, 228], [227, 230, 228], [219, 222, 220], [226, 229, 227], [232, 235, 233], [236, 239, 237], [236, 239, 237], [236, 239, 237], [234, 237, 235], [234, 237, 235]]

# 將列表轉換為 NumPy 陣列
image1_array = np.array(image1_values)
image2_array = np.array(image2_values)

# 計算兩個圖像對應像素之間的差異
difference = np.abs(image1_array - image2_array)

# 定義一個閾值，用於判斷像素值差異是否足夠小，以確定兩個圖像在該位置上相似
threshold = 5

# 判斷差異是否小於閾值，並找出相似的地方
similar_points = np.where(difference < threshold)

# 輸出相似的點位
print("相似的點位：")
for i in range(len(similar_points[0])):
    x = similar_points[0][i]
    y = similar_points[1][i]
    print("位置 ({}, {})".format(x, y))
